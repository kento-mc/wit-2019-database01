DROP SCHEMA IF EXISTS hospital;

CREATE SCHEMA IF NOT EXISTS hospital;

USE hospital;
DROP TABLE IF EXISTS Ward;
DROP TABLE IF EXISTS Patient;
DROP TABLE IF EXISTS Admission;
DROP TABLE IF EXISTS Bed;
DROP TABLE IF EXISTS Doctor;
DROP TABLE IF EXISTS Drug;
DROP TABLE IF EXISTS Visit;
DROP TABLE IF EXISTS Prescription;

CREATE TABLE IF NOT EXISTS Ward
(
wardID INT PRIMARY KEY AUTO_INCREMENT,
wardName VARCHAR(20),
wardType ENUM('OBGYN', 'Pediatric', 'Oncology', 'Emergency', 'Cardiology', 'ICU', 'Neurology', 'General')
);


CREATE TABLE IF NOT EXISTS Patient
(
patientID INT PRIMARY KEY AUTO_INCREMENT, 
fName VARCHAR(20), 
lName VARCHAR(20), 
street VARCHAR(50), 
town VARCHAR(20), 
county VARCHAR(20), 
contactNo BIGINT,
DOB DATE
);

CREATE TABLE IF NOT EXISTS Admission
(
admitID INT PRIMARY KEY AUTO_INCREMENT,
wardID INT,
inDate DATE,
inTime TIME,
outDate DATE,
outTime TIME,
patientID INT,
FOREIGN KEY (wardID) REFERENCES WARD(wardID)ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (patientID) REFERENCES Patient(patientID) ON UPDATE CASCADE ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS Bed
(
bedNumber INT PRIMARY KEY AUTO_INCREMENT,
bedType ENUM('big', 'small', 'maternity'), 
wardID INT, 
admitID INT,
FOREIGN KEY (wardID) REFERENCES Ward(wardID) ON UPDATE CASCADE ON DELETE SET NULL,
FOREIGN KEY (admitID) REFERENCES Admission(admitID) ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS Doctor
(
PPS VARCHAR(15) PRIMARY KEY, 
fName VARCHAR(15), 
lName VARCHAR(15), 
street VARCHAR(20), 
town VARCHAR(20), 
county VARCHAR(20), 
contactNo INT, 
hireDate DATE,
specialisation ENUM('General', 'Pediatrics', 'Oncology', 'ENT', 'OBGYN', 'Incompetence', 'Psychotherapy', 'Plastic Surgery')
);

CREATE TABLE IF NOT EXISTS Drug
(
drugID INT PRIMARY KEY AUTO_INCREMENT, 
drugName VARCHAR(20), 
manufacturer VARCHAR(20)
);

CREATE TABLE IF NOT EXISTS Visit
(
visitID INT PRIMARY KEY AUTO_INCREMENT, 
admitID INT NOT NULL, 
PPS VARCHAR(20), 
date DATE, 
time TIME,
FOREIGN KEY (admitID) REFERENCES Admission(admitID) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (PPS) REFERENCES DOCTOR(PPS) ON UPDATE CASCADE ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS Prescription
(
visitID INT NOT NULL, 
drugID INT NOT NULL, 
dosageDetails VARCHAR(120),
PRIMARY KEY (visitID, drugID),
FOREIGN KEY (visitID) REFERENCES Visit(visitID) ON UPDATE CASCADE ON DELETE CASCADE,
FOREIGN KEY (drugID) REFERENCES Drug(drugID) ON UPDATE CASCADE ON DELETE CASCADE
);

